"""5.Use list comprehension to construct a new list but add 6 to each item.
	list = [24,34,54,45]
"""

list_items = [24,34,54,45]
# Using list comprehension to create a new list by adding 6 to each item in the original list
new_list = [x + 6 for x in list_items]

print("New List after adding 6 to each item:", new_list)




